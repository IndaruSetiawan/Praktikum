txt = 'Hello World'

#Hitung Jumlah Karakter
print(len(txt))

#Ambil karakter terakhir
print(txt[-1])

#Ambil index ke2 sampai ke4
print(txt[2:5])

#Hilangkan spasi
print(txt.replace(" ", ""))

#Ubah text menjadi huruf besar
print(txt.upper())

#Ubah text menjadi huruf kecil
print(txt.lower())

#Ganti karakter H dengan J
print(txt.replace("H", "J"))